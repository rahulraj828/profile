// Add smooth scrolling to nav links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();

        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});

// Add a simple animation to skills
window.addEventListener('load', () => {
    const skills = document.querySelectorAll('.skills-list li');
    skills.forEach((skill, index) => {
        skill.style.opacity = '0';
        skill.style.transform = 'translateY(20px)';
        setTimeout(() => {
            skill.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
            skill.style.opacity = '1';
            skill.style.transform = 'translateY(0)';
        }, index * 100);
    });
});

// Add a scroll reveal effect for sections
const revealSection = (entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('reveal');
            observer.unobserve(entry.target);
        }
    });
};

const sectionObserver = new IntersectionObserver(revealSection, {
    root: null,
    threshold: 0.15,
});

document.querySelectorAll('section').forEach(section => {
    section.classList.add('hidden');
    sectionObserver.observe(section);
});

// Typing effect for the name in the hero section
const nameElement = document.querySelector('#home h1');
const nameText = nameElement.textContent;
nameElement.textContent = '';

const typeWriter = (text, i = 0) => {
    if (i < text.length) {
        nameElement.textContent += text.charAt(i);
        setTimeout(() => typeWriter(text, i + 1), 100);
    }
};

window.addEventListener('load', () => {
    setTimeout(() => typeWriter(nameText), 500);
});

